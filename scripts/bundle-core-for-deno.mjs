// Bundles @wrad/core into a single self-contained ESM file the verify-scores
// Supabase edge function can import under Deno (core's extensionless relative
// imports don't resolve there; a pre-bundled file sidesteps runtime
// resolution entirely). Rerun after any core change, commit the output:
//   node scripts/bundle-core-for-deno.mjs
import { build } from 'esbuild';

await build({
  entryPoints: ['packages/core/src/index.ts'],
  bundle: true,
  format: 'esm',
  platform: 'neutral',
  target: 'es2022',
  outfile: 'supabase/functions/verify-scores/wrad-core.bundle.mjs',
  banner: {
    js: '// GENERATED by scripts/bundle-core-for-deno.mjs — do not edit by hand.',
  },
});
console.log('wrote supabase/functions/verify-scores/wrad-core.bundle.mjs');
